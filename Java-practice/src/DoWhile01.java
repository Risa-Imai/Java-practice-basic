
public class DoWhile01 {
	public static void main(String[] args) {
		int number = 1;
		do {
			number *= 2;
			System.out.println("DoWhile01 = " + number);
		} while (number < 50);
		int number1 = 100;
		do {
			number1 *= 2;
			System.out.println("DoWhile01 = " + number1);
		} while (number1 < 50);
	}
}
